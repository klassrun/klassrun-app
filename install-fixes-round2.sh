write_file() {
  local target="$1"
  local b64="$2"
  if [ -f "$target" ]; then
    local backup_path="$BACKUP_DIR/$target"
    mkdir -p "$(dirname "$backup_path")"
    cp "$target" "$backup_path"
  fi
  mkdir -p "$(dirname "$target")"
  echo "$b64" | base64 -d > "$target"
  echo "  ✓ $target"
}

